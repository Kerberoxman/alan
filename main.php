			<h1>CLOUD COMPUTING</h1>
			<H2>Definición y características de Cloud Computing</H2>
			<p><li>No hay definición estándar aceptada universalmente; sin embargo, existen organismos internacionales cuyos objetivos son la estandarización de TI y, en particular, de Cloud Computing.</li></p>
			<p><li>Uno de ellos el National Institute of Standards and Technology (NIST) que define la computación en nube (cloud computing)  como: </li></p>
			<p>		“Un conjunto de hardware y software, almacenamiento, servicios e interfaces que facilitan la entrada de la información como un servicio”</p>
			<p><li>Según  NIST, el modelo se compone de cinco características esenciales,  tres modelos de servicio y cuatro modelos de despliegue.</li></p>
			<p><li>Los servicios de la nube incluyen el software, infraestructura y almacenamiento en Internet, bien como componentes independientes o como una plataforma completa basada en la demanda del usuario.</li></p>
			<H2>Actores de Cloud Computing</H2>
			<p><li>Actores o Proveedores: Proporcionan las aplicaciones y facilitan las tecnologías, infraestructura, plataformas y la información correspondiente.</li></p>
			<p><li>Socios de los Proveedores: Crean servicios para la nube ofreciendo y soportando servicios a los clientes.</li></p>
			<p><li>Lideres de negocio: Evalúan los servicios de la nube con el objetivo de contratarlos e implantarlos en sus organizaciones y empresas.</li></p>
			<p><li>Usuarios finales: Utilizan los servicios de la nube bien de modo gratuito o con una tarifa de pago. </li></p>
			<H2>Definición de Cloud Computing en México</H2>
			<p><li>Art 52: Reglamento de la Ley Federal de Protección de datos personales en posesión de los particulares.</li></p>
			<p><li>“Modelo de provisión externa de servicios de cómputo bajo demanda, que implica el suministro de infraestructura, plataforma o software, que se distribuye de modo flexible, mediante procedimientos de virtualización, en recursos compartidos dinámicamente”.</li></p>
			<p><li>Articulo 2 Fracción V: El esquema de Interoperabilidad y de Datos Abiertos de la Administración Pública Federal.</li></p>
			<p><li>“Modelo de prestación de servicios digitales que permite a las instituciones públicas acceder a un catálogo de servicios digitales estandarizados, los cuales pueden ser: de infraestructura como servicios, de plataforma como servicios y de software como servicios.”.</li></p>
